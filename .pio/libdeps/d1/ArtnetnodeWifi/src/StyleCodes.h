#ifndef STYLECODES_H
#define STYLECODES_H
// List of hex values and discriptions of Style Codes

#define StNode       0x00  // A DMX to / from Art-Net device
#define StController 0x01  // A lighting console.
#define StMedia      0x02  // A Media Server.
#define StRoute      0x03  // A network routing device.
#define StBackup     0x04  // A backup device.
#define StConfig     0x05  // A configuration or diagnostic tool.
#define StVisual     0x06  // A visualiser.

#endif